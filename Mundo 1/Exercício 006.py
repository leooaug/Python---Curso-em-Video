#DOBRO, TRIPLO E RAÍZ QUADRADA DO NÚMERO DIGITADO.

num = int(input('Digite um número para saber seu dobro, triplo e raíz quadrada: '))
dobro = num * 2
triplo = num * 3
raizq = pow(num,(1/2))
print('O dobro do número digitado é {}'.format(dobro))
print('O triplo do número digitado é {}'.format(triplo))
print('A raiz quadrada do número digitado é {}'.format(raizq))
