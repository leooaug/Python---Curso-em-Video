# PROGRAMA LÊ UM ÂNGULO E MOSTRA O VALOR DO SEU SENO, COSSENO E TANGENTE.
import math
ang = float(input('Digite um ângulo qualquer para saber seu seno, cosseno e tangente: '))
print('Seno: {:.2f}'.format(math.sin(ang)))
print('Cosseno: {:.2f}'.format(math.cos(ang)))
print('Tangente: {:.2f}'.format(math.tan(ang)))

